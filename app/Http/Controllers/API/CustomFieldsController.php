     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(CustomFieldsRequest $request)
    {
        $data = CustomFields::create([
            'parent_id' => $request->input('source_of_funds.value'),
            'code' => $request->input('code'),
            'fields' => collect($request->input('fields')),
        ]);
        return $this->sendResponse($data, "Custom Fields has been added.");
    }

